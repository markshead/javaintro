package javaintro.ps02;

/*
 * Primitives are data types like int and boolean. In this exercise we are going to look at char and boolean primitives.
 * You can read more about primitives here:
 * http://download.oracle.com/javase/tutorial/java/nutsandbolts/datatypes.html
 */
public class ProblemSet02Exercise07 {
    
    /*
     * We have looked at boolean primitives before.  Boolean primitves can hold
     * the value of either true or false.  
     * 
     * Fix the method below so it returns true instead of false.
     */
    public static boolean getBooleanTrue() {
        boolean returnValue = false;
        return returnValue;
    }
    
    /* 
     * A char primitive type can hold a single character. Notice that the char must be enclosed by single quotes instead of 
     * double quotes.  A single letter enclosed by quotation marks (double quotes) is a String, while a single character 
     * enclosed by single quotes is a char.
     * 
     * Change this method to return the char with a value of A.
     */
    public static char getA() {
        char aChar = 'B';
        return aChar;
    }
    
    /* 
     * Change this method to return a char with a value of Z.
     */
    public static char getZ() {
        char aChar = 'Y';
        return aChar;
    }
    
 
     
}
